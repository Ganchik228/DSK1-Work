from fastapi import APIRouter, Depends, HTTPException, status
from fastapi.security import OAuth2PasswordBearer, OAuth2PasswordRequestForm

from passlib.context import CryptContext
from jose import JWTError, jwt
from datetime import datetime, timedelta, timezone

from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy.future import select
from models.db_model import UserAuth, get_db, AuthLogs
import uuid

from pydantic import BaseModel

from dotenv import load_dotenv
import os

load_dotenv()

router = APIRouter(prefix="/api/auth")

class RegData(BaseModel):
    username: str
    password: str

SECRET_KEY = os.getenv("SECRET_KEY")
ALGORITHM = os.getenv("ALGORITHM")
ACCESS_TOKEN_EXPIRE_MINUTES = 180
oauth2_scheme = OAuth2PasswordBearer(tokenUrl="token")


async def get_current_user(token: str = Depends(oauth2_scheme), db: AsyncSession = Depends(get_db)):
    credentials_exception = HTTPException(
        status_code=status.HTTP_401_UNAUTHORIZED,
        detail="ошибка валидации",
        headers={"WWW-Authenticate": "Bearer"},
    )
    try:
        payload = jwt.decode(token, SECRET_KEY, algorithms=[ALGORITHM])
        username: str = payload.get("sub")
        if username is None:
            raise credentials_exception
    except JWTError:
        raise credentials_exception
    
    result = await db.execute(select(UserAuth).where(UserAuth.login == username))
    user = result.scalar_one_or_none()
    if user is None:
        raise credentials_exception
    return user

pwd_context = CryptContext(schemes=["bcrypt"], deprecated="auto")

def verify_password(plain_password, hashed_password):
    return pwd_context.verify(plain_password, hashed_password)

def get_password_hash(password):
    return pwd_context.hash(password)

def create_access_token(data: dict, expires_delta: timedelta | None = None):
    to_encode = data.copy()
    expire = datetime.now(timezone.utc) + (expires_delta or timedelta(minutes=15))
    to_encode.update({"exp": expire})
    return jwt.encode(to_encode, SECRET_KEY, algorithm=ALGORITHM)

@router.post("/register")
async def register(data: RegData, db: AsyncSession = Depends(get_db)):
    username = data.username
    password = data.password

    if not username or not password:
        raise HTTPException(status_code=400, detail="Логин и пароль не могут быть пустыми")
    
    result = await db.execute(select(UserAuth).where(UserAuth.login == username))
    user = result.scalar_one_or_none()

    if user:
        raise HTTPException(status_code=400, detail="Пользотватель с таким именем уже существует")
    
    hashed_pwd = get_password_hash(password)
    new_user = UserAuth(id=uuid.uuid1(), login=username, password=hashed_pwd)
    db.add(new_user)
    await db.commit()
    return {"message": "Пользователь успешно зарегистрирован"}

@router.post("/token")
async def login(form_data: OAuth2PasswordRequestForm = Depends(), db: AsyncSession = Depends(get_db)):
    result = await db.execute(select(UserAuth).where(UserAuth.login == form_data.username))
    user = result.scalar_one_or_none()
    if not user or not verify_password(form_data.password, user.password):
        raise HTTPException(status_code=401, detail="Нверный логин или пароль")
    
    log = AuthLogs(
        id=uuid.uuid1(),
        user_auth_id=user.id,
        date=datetime.now()
    )
    db.add(log)
    
    access_token = create_access_token(data={"sub": user.login})
    await db.commit()
    return {"access_token": access_token}
